console.log( x == "1"); //loose comparison, means it compares the value
console.log( x === 1 ); //stric comparison, means it compares the data and the typeconsole.log( x === 1 ); //stric comparison, means it compares the data and the type
console.log( x === "1" ); //it's false because, even the value of x is 1, the type is number and not string